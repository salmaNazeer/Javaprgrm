package sampleproject;

public class Swap {
public static void main(String []args){
int num1=30;
int num2=40;
System.out.println("before swapping");
System.out.println("num1=" +num1);
System.out.println("num2=" +num2);
int temp=num1;
num1=num2;
num2=temp;
System.out.println("after Swapping");
System.out.println("num1=" +num1);
System.out.println("num2=" +num2);
}
}
